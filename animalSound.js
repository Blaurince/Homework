

function animalSounds(animal){

switch( animal){
case 'dog':
        console.log('woof');
        break
    

case 'cat':
    console.log('meow');
    break

case 'owl':
        console.log('hoot');
        break


default:
    console.log('undefined');
}

}
animalSounds('dog');
